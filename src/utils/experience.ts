/**
 * Розраховує кількість досвіду, необхідну для досягнення наступного рівня.
 * @param level - Поточний рівень гравця.
 * @returns Кількість досвіду (XP), яка потрібна для переходу на наступний рівень.
 */
export const getRequiredExp = (level: number): number => {
    // Проста експоненційна формула:
    // Рівень 1 -> 2: 100 XP
    // Рівень 2 -> 3: 200 XP
    // Рівень 3 -> 4: 400 XP
    // і так далі...
    return 100 * Math.pow(2, level - 1);
};